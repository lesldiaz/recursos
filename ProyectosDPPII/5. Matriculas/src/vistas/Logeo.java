    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
        String usuario = tfusuario.getText();
        String pass = pfpass.getText();
        boolean tipo = tbalumno.isSelected();
        campos.add(usuario);
        campos.add(pass);
        camposV = validar.camposVacios(campos);
        if (!camposV.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campos Vacios");
            camposV.clear();
        } else {
            String passEncriptada = validar.encriptaEnMD5(pass);
            String pass2xEncriptada = validar.encriptaEnMD5(passEncriptada);
            String nombreyApellido = validar.buscarUsuario(usuario, pass2xEncriptada, tipo);
            if (nombreyApellido.isEmpty()){
                JOptionPane.showMessageDialog(null, "Usuario no existe");
            } else{
                if (tipo){//estudiante
                    //llamar a la pantalla estudiante
                    Alumno alumno = new Alumno(nombreyApellido);
                    alumno.setVisible(true);
                    dispose();
                } else{
                    //llamar a la pantalla docente
                    Docente docente = new Docente(nombreyApellido);
                    docente.setVisible(true);
                    dispose();
                }
//                JOptionPane.showMessageDialog(null, "Si existe usuario, abrir otra pantalla para: " + nombreyApellido);
            }
        }
    }//GEN-LAST:event_btningresarActionPerformed

