    public void actualizarLista(List lista, JList listaVista, boolean tipo) {
        //llamamos al modelo por defecto
        DefaultListModel model = new DefaultListModel();
        for (int i = 0; i < lista.size(); i++) {
            if (tipo) {
                Materia materia = (Materia) lista.get(i);
                model.addElement(materia);
            } else {
                Horario horario = (Horario) lista.get(i);
                model.addElement(horario);
            }
        }
        listaVista.setModel(model);
        listaVista.revalidate();
    }

