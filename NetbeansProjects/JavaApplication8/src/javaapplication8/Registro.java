    private void registrarEstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarEstActionPerformed
        String datos = cedulaEst.getText()+","+numeroUnico.getText()+","+nombreEst.getText()+","+apellidoEst.getText();
        Estudiante est = new Estudiante(datos);
        est.setEstudiantes(est);
		Collections.sort(est.getEstudiantes(), new Comparator<Estudiante>(){
			public int compare(Estudiante o1, Estudiante o2) {
				return o1.getNombre().compareTo(o2.getNombre());
			}
		});
		
		for(Estudiante est1 : est.getEstudiantes()){
			listaEstudiantes.setText(est.getEstudiantes().toString());
		}
        cedulaEst.setText("");
        numeroUnico.setText("");
        nombreEst.setText("");
        apellidoEst.setText("");
    }//GEN-LAST:event_registrarEstActionPerformed

