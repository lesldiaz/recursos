    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (area1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Complete los espacios en blanco");
        } else if (rbtexto.isSelected()) {
            String palabra = area1.getText();
            palabra = palabra.toLowerCase();
            new TraductorTexto().traducirTextoMorse(palabra, area2);

        } else if (rbmorse.isSelected()) {
            String palabra = area1.getText();
            new TraductorMorse().traducirMorseTexto(palabra, area2);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

