    private void area1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_area1KeyTyped
        if (rbtexto.isSelected()) {
            char car = evt.getKeyChar();
            if ((car < '0' || car > '9') && (car < 'a' || car > 'z') && (car < 'A' || car > 'Z') && (car != '-') && (car != 'ñ') && (car != 'é') && (car != 'á') && (car != 'í') && (car != 'ó') && (car != 'ú') && (car != 'Ñ') && (car != (char) KeyEvent.VK_BACK_SPACE) && (car != (char) KeyEvent.VK_SPACE) && (car != '.') && (car != (char) KeyEvent.VK_COMMA)) {
                getToolkit().beep();
                evt.consume();
            }
        } else if (rbmorse.isSelected()) {
            char car1 = evt.getKeyChar();
            if ((car1 != '-') && (car1 != '.') && (car1 != (char) KeyEvent.VK_BACK_SPACE) && (car1 != (char) KeyEvent.VK_SPACE)) {
                getToolkit().beep();
                evt.consume();

            }
        }
    }//GEN-LAST:event_area1KeyTyped

