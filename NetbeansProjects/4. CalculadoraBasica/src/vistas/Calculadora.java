    public void operaciones(String operador) {
        //o usar if else
        switch (operador) {
            case ("+"):
                if (operadorAux.equals("")) {
                    resultado = Double.parseDouble(auxNumeros);
                } else {
                    resultado = resultado + Double.parseDouble(auxNumeros);
                }
                auxNumeros = "";
                operadorAux = operador;
                break;
            case ("-"):
                if (operadorAux.equals("")) {
                    resultado = Double.parseDouble(auxNumeros);
                } else {
                    resultado = resultado - Double.parseDouble(auxNumeros);
                }
                auxNumeros = "";
                operadorAux = operador;
                break;
            case ("*"):
                if (operadorAux.equals("")) {
                    resultado = Double.parseDouble(auxNumeros);
                } else {
                    resultado = resultado * Double.parseDouble(auxNumeros);
                }
                auxNumeros = "";
                operadorAux = operador;
                break;
            case ("/"):
                if (operadorAux.equals("")) {
                    resultado = Double.parseDouble(auxNumeros);
                } else {
                    resultado = resultado / Double.parseDouble(auxNumeros);
                }
                auxNumeros = "";
                operadorAux = operador;
                break;
            case ("="):
                if (operadorAux.equals("")) {
                    resultado = Double.parseDouble(auxNumeros);
                    tfpantalla.setText(resultado + "");
//                    System.out.println(resultado);
//                    resultado = 0;
//                    tfpantalla.setText("");
                    operadorAux = "";
                } else {
                    operaciones(operadorAux);
                    System.out.println(resultado);
                    tfpantalla.setText(resultado + "");
//                    resultado = 0;
//                    tfpantalla.setText("");
                    operadorAux = "";
                }
                break;
        }
    }

