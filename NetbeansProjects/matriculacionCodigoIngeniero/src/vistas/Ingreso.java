    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String usuario = txtuser.getText();
        String pass = txtpass.getText();
        //
        List<String> campos = new ArrayList<>();
        campos.add(usuario);
        campos.add(pass);

        List<Integer> camposVacios = validar.camposVacios(campos);
        if (!camposVacios.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campos obligatorios vacios");//mensajes
        }
        pass = validar.encriptaEnMD5(validar.encriptaEnMD5(pass));
        if (jToggleButton1.isSelected()) {
            tipoUsuario = true;//docente
        } else {
            tipoUsuario = false;
        }
        String nombreyApellido = validar.buscarUsuario(usuario, pass, tipoUsuario);
        if (nombreyApellido.isEmpty()) {
            JOptionPane.showConfirmDialog(null, "Usuario no existe");
        } else {
            if (tipoUsuario) {
                //llamar a la pantalla docente
                Docente docente = new Docente(nombreyApellido);
                docente.setVisible(true);
                dispose();

            } else {
                //llamar a la pantalla alumno
                Alumno alumno = new Alumno(nombreyApellido);
                alumno.setVisible(true);
                dispose();
            }
        }


    }//GEN-LAST:event_jButton2ActionPerformed

