    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String x = jTextField3.getText();
        double y = Double.parseDouble(x);

        String x1 = jTextField2.getText();
        double y1 = Double.parseDouble(x1);

        String x2 = jTextField1.getText();
        double y2 = Double.parseDouble(x2);

        double k = y / y1;
        double j = 9.81;

        double y3 = (k * j / y) * ((Math.pow(y1, 2)) - (Math.pow(y2, 2)));
        double y4 = Math.sqrt(y3);

        jTextField4.setText("" + y4);
    }//GEN-LAST:event_jButton1ActionPerformed

