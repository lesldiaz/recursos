package modelo;

/**
 *
 * @author Leslie Diaz
 */
public class Vehiculo {
    private String modelo,tipo;
    

    public Vehiculo(String modelo, String tipo) {
        this.modelo = modelo;
        this.tipo = tipo;
        
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

}
