    private void bsiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bsiMouseClicked
        while (bsi.isEnabled() && bsi.isSelected()) {
            try {
                numeroTurbinas = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese el numero de turbinas por ala"));
                if (numeroTurbinas > 2 || numeroTurbinas == 0) {
                    JOptionPane.showMessageDialog(null, "Error: Limite = 2; Minimo = 1");
                    bno.doClick();
                    numeroTurbinas = 0;
                } else {
                    try {
                        potencia = Double.parseDouble(JOptionPane.showInputDialog(null, "Ingrese la potencia de las turbinas"));
                        bsi.setEnabled(false);
                        bno.setEnabled(false);
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Error", "Error", JOptionPane.ERROR_MESSAGE);
                        bno.doClick();
                        numeroTurbinas = 0;
                        potencia = 0;
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error", "Error", JOptionPane.ERROR_MESSAGE);
                bno.doClick();
                numeroTurbinas = 0;
                potencia = 0;
            }
        }
    }//GEN-LAST:event_bsiMouseClicked

