    private void baumentaraltuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_baumentaraltuActionPerformed
        if (avion.getVelocActual() >= avion.getVelocidadMin()) {

            bpararaltu.setEnabled(true);
            String interaltu = txtinteraltu.getText();
            ArrayList<String> campos = new ArrayList<>();
            campos.add(interaltu);
            List<Integer> camposVacios = validar.camposVacios(campos);
            if (!camposVacios.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Completar los Campos Vacios");
                bpararaltu.doClick();
                for (Integer camposVacio : camposVacios) {
                    if (null != camposVacio) {
                        switch (camposVacio) {
                            case 0:
                                txtinteraltu.setBackground(Color.red);
                                break;
                            default:
                                break;
                        }
                    }
                }
            } else {
                hiloa = new HiloAltura(barraaltura, txtalturaactual, interaltu, avion, (int) avion.getAltuActual());
                hiloa.start();
                txtinteraltu.setEditable(false);
                if (hiloa.isEstado() == true) {
                    baumentaraltu.setEnabled(false);

                } else {
                    baumentaraltu.setEnabled(true);
                }

                if (avion.getAltuActual() <= avion.getAlturaMax() && avion.getAltuActual() > 0) {
                    bdescenso.setEnabled(true);
                    
                }

            }
        }
    }//GEN-LAST:event_baumentaraltuActionPerformed

