    public MostrarCasa(AuxiliarPropiedad ap) {
        initComponents();
        this.setSize(800, 600);
        setAlwaysOnTop(false);
        setLocationRelativeTo(null);
        setResizable(false);  // no me deja maximizar
        cdt= pbd.informacionPropiedad(ap);
        if (cdt.getNumeropisos()==0 && cdt.getPiso()==0){
            lbltipop.setText(cdt.getTipop());
            lblpiso.setText("");
            lblnump.setText("");
            lblnumban.setText("");
            lblnumhab.setText("");
        } else if (cdt.getNumeropisos()==0){
            lbltipop.setText(cdt.getTipop());
            lblpiso.setText(cdt.getPiso()+"");
           lblnump.setText("");
        } else {
            lblpiso.setText(cdt.getNumeropisos()+"");
            lbltipop.setText(cdt.getTipop());
            lblpiso.setText("");
        }
        lblprecio.setText(cdt.getPrecio()+"");
        lbltipoc.setText(cdt.getTipocontrato());
        lblnombre.setText(cdt.getNombre());
        lblcalleprincipal.setText(cdt.getCalleprincipal());
        lblcalles.setText(cdt.getCallesecundaria());
        lblsector.setText(cdt.getSector());
        lblnumpropiedad.setText(cdt.getNumerocasa());
        lblextension.setText(cdt.getExtension()+"");
        lblnumhab.setText(cdt.getNumerocuartos()+"");
        lblnumban.setText(cdt.getNumerobanios()+"");
        lbldescripcion.setText(cdt.getDescripcion());
        cbpatio.setSelected(cdt.isPatio());
        cbparqueadero.setSelected(cdt.isParqueadero());
    }

