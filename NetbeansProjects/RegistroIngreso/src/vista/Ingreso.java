    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String usuarioini = txtusuarioin.getText();
        String contraseñaini = txtcontrasin.getText();
        
        
        ArrayList<String> campos = new ArrayList<>();
        campos.add(usuarioini);
        campos.add(contraseñaini);

        List<Integer> camposVacios = validar.camposVacios(campos);
        if (!camposVacios.isEmpty()) {  //!camposVacios.isEmpty()
            JOptionPane.showMessageDialog(null, "Completar los Campos Vacios");
        }
        
        contraseñaini = validar.encriptaEnMD5(validar.encriptaEnMD5(contraseñaini));
        
        if(todocente.isSelected()){
            tipoUser = true;
        } else {
            tipoUser =false;
        }
        
        String nombreyApellido = validar.buscarUsuario(usuarioini,contraseñaini,tipoUser);
        
        if(nombreyApellido.isEmpty()){
            JOptionPane.showMessageDialog(null, "Usuario No Existe");
        }else {
            if(tipoUser){
                Docente docente = new Docente(nombreyApellido);
                docente.setVisible(true);
                dispose();
            } else{
                Alumno alumno = new Alumno (nombreyApellido);
                alumno.setVisible(true);
                dispose();
            }
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

