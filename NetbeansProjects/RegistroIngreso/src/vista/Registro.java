    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombreApellido = txtnombres.getText();
        String usuario = txtusuario.getText();
        String contrase単a = txtcontras.getText();

        ArrayList<String> campos = new ArrayList<>();
        
        
        campos.add(nombreApellido);
        campos.add(usuario);
        campos.add(contrase単a);

        List<Integer> camposVacios = validar.camposVacios(campos);
        if (!camposVacios.isEmpty()) {  //!camposVacios.isEmpty()
            JOptionPane.showMessageDialog(null, "Completar los Campos Vacios");
            for (Integer camposVacio : camposVacios) {
                if (camposVacio == 0) {
                    txtnombres.setBackground(Color.red);
                } else {
                    if (camposVacio == 1) {
                        txtusuario.setBackground(Color.red);
                    } else {
                        if (camposVacio == 2) {
                            txtcontras.setBackground(Color.red);
                        } else {

                        }
                    }
                }
            }
        } else {
            contraencriptada = validar.encriptaEnMD5(contrase単a);
            contraencriptada = validar.encriptaEnMD5(contraencriptada);
            System.out.println(contraencriptada);
            
        boolean tipo = true;
            if (bdocente.isSelected()) {
                tipo = true;
            } else if (bestudiante.isSelected()) {
                tipo = false;
            }
         Usuario usuarionuevo = new Usuario(nombreApellido, usuario, contraencriptada, tipo);
         
         if(leeresc.leerArchivo("listaUsuarios")==null){
             listaUsuario.add(usuarionuevo);
             leeresc.escribirarchivo(listaUsuario,"listaUsuarios");
         }else{
             listaUsuario = (ArrayList<Usuario>) leeresc.leerArchivo("listaUsuarios");
             listaUsuario.add(usuarionuevo);
             leeresc.escribirarchivo(listaUsuario, "listaUsuarios");
         }  
            JOptionPane.showMessageDialog(null, "Usuario Registrado");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

