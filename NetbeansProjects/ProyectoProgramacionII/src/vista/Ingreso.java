    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String usuario = txtusuario.getText();
        String contrase単a = validar.encriptaEnMD5(validar.encriptaEnMD5(txtpass.getText()));
        ArrayList<String> campos = new ArrayList<>();
        campos.add(usuario);
        campos.add(contrase単a);

        List<Integer> camposVacios = validar.camposVacios(campos);
        if (!camposVacios.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Completar los Campos Vacios");
            for (Integer camposVacio : camposVacios) {
                if (null != camposVacio) {
                    switch (camposVacio) {
                        case 0:
                            txtusuario.setBackground(color);
                            break;
                        case 1:
                            txtpass.setBackground(color); // recordatorio: no se pinta de rojo
                            break;
                        default:
                            break;
                    }
                }
            }
        } else if (validar.isEmail(usuario) == false) {
            JOptionPane.showMessageDialog(null, "Email Incorrecto");
        }
         


    }//GEN-LAST:event_jButton1ActionPerformed

