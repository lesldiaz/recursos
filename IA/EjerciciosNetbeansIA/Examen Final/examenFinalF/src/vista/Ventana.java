    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        i++;
        band = (String) banda.getSelectedItem();
        memo = (String) mem.getSelectedItem();
        cp = (String) cpu.getSelectedItem();
        bander = (String) flag.getSelectedItem();
        switch(i){
            case 1:
                pred=rb.predecir(band, memo, cp, bander);
                JOptionPane.showMessageDialog(null, "Primera Entrada / 2 restantes");
                break;
            case 2:
                pred1=rb.predecir(band, memo, cp, bander);
                JOptionPane.showMessageDialog(null, "Segunda Entrada / 1 restantes");
                break;
            case 3:
                pred2=rb.predecir(band, memo, cp, bander);
                JOptionPane.showMessageDialog(null, "Tercera Entrada / 0 restantes");
                lista.add(pred);
                lista.add(pred1);
                lista.add(pred2);
                dispose();
                break;
            default:
                break;
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

