     public void escribirarchivo(String lista) {
        try {
            String ruta = "src/datos/irisPredic.arff";
            File archivo = new File(ruta);
            BufferedWriter bw;
            if(archivo.exists()) {
                bw = new BufferedWriter(new FileWriter(archivo));
                bw.write(lista);
            } else {
                bw = new BufferedWriter(new FileWriter(archivo));
                bw.write(lista);
            }
            bw.close();
        } catch (IOException ex) {
            Logger.getLogger(Prediccion.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
    
     
    public String resultado(double pred){
        String res = "";
        if (pred == 0.0){
            res = "Iris-setosa";
        }else if (pred == 1.0){
            res= "Iris-versicolor";
        }else if (pred == 2.0){
            res = "Iris-virginica";
        }else {
            res = "Desconocido";
        }
      return res;  
    }
